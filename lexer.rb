    def get_token
      token = []
      @current_state = 0
      @last_valid_token = []
      @last_final_state_cursor = 0
      @last_final_state = -1
      until @file.eof?
        c = @file.getc
        ch = c.ord

        if @file.eof?
          if @final_states[@current_state] == -1
            if @last_final_state != -1
              @file.seek(@last_final_state_cursor+1, IO::SEEK_SET)
              @current_state = @last_final_state
              token = @last_valid_token
