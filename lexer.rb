              @current_state = @last_final_state
              token = @last_valid_token
            else
              @current_state = 0
              return Token.new(-1, @file.pos)
            end
          end

          if @final_states[@current_state] >= 0 # e stare finala
            token = token.join('')
            if !@token_list.include? token
              @token_list << token
